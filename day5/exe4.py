dayno=int(input("number of the day"))
if dayno <=0 or dayno>7:
    print("Error in searching for the day")
elif dayno==1:
    print("Sunday")
elif dayno==2:
    print("Monday")
elif dayno==3:
    print("Tuesday")
elif dayno==4:
    print("Wednesday")
elif dayno==5:
    print("Thursday")
elif dayno==6:
    print("Friday")
elif dayno==7:
    print("Saturday")


